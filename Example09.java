public class Example09
{
  public static void main(String[] args)
  {
    int grade=75;
    if(grade>80)
    {
      System.out.println("该学生成绩为优");
    }
    else if(grade>70)
    {
      System.out.println("该学生成绩为良");
    }
    else if(grade>60)
    {
      System.out.println("该生成绩为中");
    }
    else
    {
      System.out.println("该生成绩为差");
    }
  }
}